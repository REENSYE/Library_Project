    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        String e = jComboBox1.getSelectedItem().toString();

        if(e.equals("اضافة كتاب")){
            this.dispose();
            new AddBook().setVisible(true);
        }else if(e.equals("اضافة رسالة")){
            this.dispose();
            new AddMsg().setVisible(true);
        }else if(e.equals("اضافة دورية")){
            this.dispose();
            new AddDawryah().setVisible(true);
        }else if(e.equals("اضافة مشروع")){
            this.dispose();
            new AddProject().setVisible(true);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

